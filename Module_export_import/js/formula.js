function sum(x, y) {
    return Number(x) + Number(y);
}

function multiply(x, y) {
    return x * y;
}

function devide(x, y) {
    return Number(x) / Number(y);
}

function minus(x, y) {
    return Number(x) - Number(y);
}

/* we export our functions add and multiply */
export { sum, multiply, devide, minus };